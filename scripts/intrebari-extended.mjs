// Helper: generate class-specific variations
function withClase(base, clase) {
  const result = [base];
  for (const cls of clase) {
    result.push(`${base} clasa a ${cls}-a`);
  }
  return result;
}

// Helper: generate "exercitii rezolvate" variations
function withExercitii(topics) {
  const result = [];
  for (const t of topics) {
    result.push(t);
    result.push(`Exerciții rezolvate ${t.toLowerCase().replace(/^(ce este |cum |care |cand |cat )/i, '')}`);
  }
  return result;
}

